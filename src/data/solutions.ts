import type { ThreeBodySolution } from "@/types";

const palette = ["#f05d5e", "#2d9cdb", "#f2c94c"];
const spatialPalette = ["#ff8a5b", "#67d5b5", "#b38cff"];
const lagrangeAngularVelocity = 3 ** -0.25;

function equalMassCollinearOrbit(
  slug: string,
  p1: number,
  p2: number,
  period: number,
  viewScale: number,
  trailLength: number,
  recommendedDt = 0.004,
  integrationDt = recommendedDt,
  stability: ThreeBodySolution["stability"] = "unverified",
): ThreeBodySolution {
  return {
    slug,
    dimension: "2d",
    orbitClass: "periodic",
    stability,
    recommendedDt,
    viewScale,
    trailLength: Math.max(trailLength, Math.ceil(period / recommendedDt) + 8),
    period,
    integrationDt,
    softening: 0,
    referenceKind: "periodic-reference",
    bodies: [
      {
        label: "A",
        mass: 1,
        color: palette[0],
        position: [-1, 0, 0],
        velocity: [p1, p2, 0],
      },
      {
        label: "B",
        mass: 1,
        color: palette[1],
        position: [0, 0, 0],
        velocity: [-2 * p1, -2 * p2, 0],
      },
      {
        label: "C",
        mass: 1,
        color: palette[2],
        position: [1, 0, 0],
        velocity: [p1, p2, 0],
      },
    ],
  };
}

function spatialPeriodicOrbit(
  slug: string,
  z0: number,
  vx: number,
  vy: number,
  vz: number,
  period: number,
  stability: "stable" | "unstable",
  viewScale: number,
  trailLength: number,
  recommendedDt = 0.003,
  integrationDt = 0.00012,
): ThreeBodySolution {
  return {
    slug,
    dimension: "3d",
    orbitClass: "periodic",
    stability,
    recommendedDt,
    viewScale,
    trailLength: Math.max(trailLength, Math.ceil(period / recommendedDt) + 8),
    period,
    integrationDt,
    softening: 0,
    referenceKind: "periodic-reference",
    bodies: [
      {
        label: "A",
        mass: 1,
        color: spatialPalette[0],
        position: [-1, 0, 0],
        velocity: [vx, vy, vz],
      },
      {
        label: "B",
        mass: 1,
        color: spatialPalette[1],
        position: [1, 0, 0],
        velocity: [vx, vy, -vz],
      },
      {
        label: "C",
        mass: 1,
        color: spatialPalette[2],
        position: [0, 0, z0],
        velocity: [-2 * vx, -2 * vy, 0],
      },
    ],
  };
}

